package cleanup

import (
	"fmt"
	"io/fs"
	"os"
	"path/filepath"
	"strings"
)

func RemoveNodeModules(root string) error {
	return filepath.WalkDir(root, func(path string, d fs.DirEntry, err error) error {
		if err != nil {
			return err
		}

		name := d.Name()
		if name == ".git" {
			return filepath.SkipDir
		}

		if name != "node_modules" {
			return nil
		}

		if err := removeNodeModulesPath(path); err != nil {
			return err
		}

		if d.IsDir() {
			return filepath.SkipDir
		}
		return nil
	})
}

func removeNodeModulesPath(path string) error {
	info, err := os.Lstat(path)
	if err != nil {
		if os.IsNotExist(err) {
			return nil
		}
		return fmt.Errorf("stat %s: %w", path, err)
	}

	if info.Mode()&os.ModeSymlink != 0 {
		if err := os.Remove(path); err != nil && !os.IsNotExist(err) {
			return fmt.Errorf("remove symlink %s: %w", path, err)
		}
		return nil
	}

	if err := os.RemoveAll(path); err != nil && !os.IsNotExist(err) {
		return fmt.Errorf("remove %s: %w", path, err)
	}

	return nil
}

func LooksLikeNodeProject(root string) bool {
	markers := []string{"package.json", "pnpm-lock.yaml", "package-lock.json", "yarn.lock"}
	for _, marker := range markers {
		if _, err := os.Stat(filepath.Join(root, marker)); err == nil {
			return true
		}
	}

	return strings.Contains(root, "node")
}
