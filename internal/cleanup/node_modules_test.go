package cleanup

import (
	"os"
	"path/filepath"
	"testing"
)

func TestRemoveNodeModulesRemovesDirectoriesAndSymlinks(t *testing.T) {
	root := t.TempDir()
	nested := filepath.Join(root, "packages", "ui", "node_modules")
	if err := os.MkdirAll(nested, 0o755); err != nil {
		t.Fatalf("mkdir nested node_modules: %v", err)
	}
	if err := os.WriteFile(filepath.Join(nested, "file.txt"), []byte("x"), 0o644); err != nil {
		t.Fatalf("write nested file: %v", err)
	}

	shared := filepath.Join(root, "shared_modules")
	if err := os.MkdirAll(shared, 0o755); err != nil {
		t.Fatalf("mkdir shared modules: %v", err)
	}
	if err := os.Symlink(shared, filepath.Join(root, "node_modules")); err != nil {
		t.Fatalf("symlink node_modules: %v", err)
	}

	gitNodeModules := filepath.Join(root, ".git", "node_modules")
	if err := os.MkdirAll(gitNodeModules, 0o755); err != nil {
		t.Fatalf("mkdir git node_modules: %v", err)
	}

	if err := RemoveNodeModules(root); err != nil {
		t.Fatalf("remove node_modules: %v", err)
	}

	if _, err := os.Lstat(filepath.Join(root, "node_modules")); !os.IsNotExist(err) {
		t.Fatalf("expected root node_modules symlink removed, got err=%v", err)
	}
	if _, err := os.Stat(nested); !os.IsNotExist(err) {
		t.Fatalf("expected nested node_modules removed, got err=%v", err)
	}
	if _, err := os.Stat(gitNodeModules); err != nil {
		t.Fatalf("expected .git/node_modules untouched, got err=%v", err)
	}
}
